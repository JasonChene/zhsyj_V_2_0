DROP FUNCTION IF EXISTS `f_getSequenceStr`;

CREATE DEFINER = 'root'@'%'
FUNCTION f_getSequenceStr()
  RETURNS varchar(100) CHARSET utf8
BEGIN
  DECLARE v_ymdhns VARCHAR(14);#获取年月日时分秒
  DECLARE v_random varchar(11);
  DECLARE v_random2 varchar(11);
  declare v_ret varchar(25);

  SELECT DATE_FORMAT(NOW(),'%Y%m%d%H%i%S')
  INTO v_ymdhns;

  SELECT SUBSTRING(CONCAT(FLOOR(RAND()*1000000),'00000000000'),1,6)
  INTO v_random;

  SELECT SUBSTRING(CONCAT(FLOOR(RAND()*100000),'00000000000'),1,5)
  INTO v_random2;

  SET v_ret=CONCAT(CONCAT(v_ymdhns,v_random),v_random2);

  RETURN v_ret;

END


