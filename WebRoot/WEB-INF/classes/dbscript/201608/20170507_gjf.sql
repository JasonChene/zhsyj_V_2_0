CREATE DEFINER = 'root'@'%'
FUNCTION fun_获取企业操作员编号(prm_comdm varchar(50))
  RETURNS varchar(40) CHARSET utf8
BEGIN
  declare v_maxusername VARCHAR(30); 
  declare v_three VARCHAR(30); 
    
  SELECT MAX(username) 
  INTO v_maxusername
  FROM sysuser 
  WHERE username LIKE concat(prm_comdm,'%');

  SELECT SUBSTRING(v_maxusername,10,3)
  INTO v_three;

  set v_three=RIGHT(CONCAT('000',v_three+1),3);
  set v_maxusername=CONCAT(prm_comdm,v_three);

  RETURN v_maxusername;

END


